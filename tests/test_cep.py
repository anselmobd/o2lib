from o2lib.validacao.cep import valido

def test_cep_valido():
    assert valido("12345678")
    assert valido("12345-678")
    assert valido("12.345-678")

def test_cep_invalido():
    assert not valido("")
    assert not valido(" ")
    assert not valido(" 12345678")
    assert not valido(" 12345-678")
    assert not valido(" 12.345-678")
    assert not valido("12345678 ")
    assert not valido("12345-678 ")
    assert not valido("12.345-678 ")
    assert not valido("1234")
    assert not valido("123456789")
    assert not valido("abcde123")
    assert not valido("123456-678")
    assert not valido("1234-678")
    assert not valido("12345-67")
    assert not valido("12345-6789")
    assert not valido("1.345-678")
    assert not valido("123.345-678")
    assert not valido("12.34-678")
    assert not valido("12.3456-678")
    assert not valido("12.345-67")
    assert not valido("12.345-6789")
    assert not valido("12345.678")
    assert not valido("12-345.678")
